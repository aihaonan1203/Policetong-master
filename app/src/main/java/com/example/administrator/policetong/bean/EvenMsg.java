package com.example.administrator.policetong.bean;

public class EvenMsg<T> {

   private String type;
   private T data;

    public EvenMsg(String type, T data) {
        this.type = type;
        this.data = data;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
